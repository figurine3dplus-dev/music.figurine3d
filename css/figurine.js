const itemsPerPage = 15; // 5 cărți × 3 rânduri
let currentPage = 1;

function renderBooks(page) {
    const grid = document.getElementById('books-grid');
    grid.innerHTML = '';

    const start = (page - 1) * itemsPerPage;
    const end = start + itemsPerPage;
    const pageBooks = books.slice(start, end);

    pageBooks.forEach(book => {
        const cardHTML = `
        <div class="book-card">
            <div class="book-cover-container">
                <img src="${book.image}" alt="${book.title}" loading="lazy">
            </div>
            <div class="book-info">
                <div class="book-title">${book.title}</div>
                <div class="book-price">${book.price} lei</div>
            </div>
        </div>`;
        grid.insertAdjacentHTML('beforeend', cardHTML);
    });

    document.getElementById('current-page-display').textContent = page;
}

function renderPagination() {
    const totalPages = Math.ceil(books.length / itemsPerPage);
    const container = document.getElementById('pagination');
    container.innerHTML = '';

    // Săgeată înapoi
    const prev = document.createElement('button');
    prev.className = 'arrow-btn';
    prev.innerHTML = '‹--';
    prev.disabled = currentPage === 1;
    prev.onclick = () => {
        if (currentPage > 1) {
            currentPage--;
            renderBooks(currentPage);
            renderPagination();
        }
    };
    container.appendChild(prev);

    // Numere pagini
    for (let i = 1; i <= totalPages; i++) {
        const btn = document.createElement('button');
        btn.className = `page-btn ${i === currentPage ? 'active' : ''}`;
        btn.textContent = i;
        btn.onclick = () => {
            currentPage = i;
            renderBooks(currentPage);
            renderPagination();
        };
        container.appendChild(btn);
    }

    // Săgeată înainte
    const next = document.createElement('button');
    next.className = 'arrow-btn';
    next.innerHTML = '--›';
    next.disabled = currentPage === totalPages;
    next.onclick = () => {
        if (currentPage < totalPages) {
            currentPage++;
            renderBooks(currentPage);
            renderPagination();
        }
    };
    container.appendChild(next);
}

// Inițializare la încărcare
window.addEventListener('load', () => {
    renderBooks(currentPage);
    renderPagination();
    
    // Re-aplicăm scroll reveal-ul existent
    const reveals = document.querySelectorAll('.reveal');
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) entry.target.classList.add('visible');
        });
    }, { threshold: 0.12 });
    reveals.forEach(el => observer.observe(el));
});